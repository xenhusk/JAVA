public class BreakContinue {
    public static void main(String[]args){
        int x,y;
        System.out.println("Multiplication table from 1-15");
        for (x=1;x<=5;x++){
            for (y=1;y<=5;y++){
                    System.out.print(x*y+"\t");
            }
            System.out.println();
        }
    }
}
